<?php
//error_reporting(0);

// Name of the file
$filename = 'innovcart.sql';
// MySQL host
$mysql_host = $_SESSION['dbhost'];
// MySQL username
$mysql_username = $_SESSION['dbuser'];
// MySQL password
$mysql_password = $_SESSION['dbpass'];
// Database name
$mysql_database = $_SESSION['dbname'];

// Connect to MySQL server
$conn=new mysqli($mysql_host, $mysql_username, $mysql_password, $mysql_database) or die('Error connecting to MySQL server: ' . mysql_error());
// Select database
//mysql_select_db($mysql_database) or die('Error selecting MySQL database: ' . mysql_error());

// Temporary variable, used to store current query
$templine = '';
// Read in entire file
$lines = file($filename);
// Loop through each line
foreach ($lines as $line)
{
// Skip it if it's a comment
if (substr($line, 0, 2) == '--' || $line == '')
    continue;

// Add this line to the current segment
$templine .= $line;
// If it has a semicolon at the end, it's the end of the query
if (substr(trim($line), -1, 1) == ';')
{
    // Perform the query
    $conn->query($templine) or print('Error performing query \'<strong>' . $templine . '\': ' . mysql_error() . '<br /><br />');
    // Reset temp variable to empty
    $templine = '';
}
}
 echo "Tables imported successfully";
?>